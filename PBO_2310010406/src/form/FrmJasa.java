    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
            int hasil = crud.simpanJasa(
                txtNamaJasa.getText(),
                cmbJenis.getSelectedItem().toString(),
                Integer.parseInt(txtHarga.getText()),
                txtKet.getText()
            );
            if (hasil > 0) {
                JOptionPane.showMessageDialog(this, "Data jasa disimpan");
                tampilData();
                kosong();
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Harga harus angka.");
        }// TODO add your handling code here:
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        if (txtIdJasa.getText().isEmpty()) return;
        try {
            int hasil = crud.ubahJasa(
                Integer.parseInt(txtIdJasa.getText()),
                txtNamaJasa.getText(),
                cmbJenis.getSelectedItem().toString(),
                Integer.parseInt(txtHarga.getText()),
                txtKet.getText()
            );
            if (hasil > 0) {
                JOptionPane.showMessageDialog(this, "Data jasa diubah");
                tampilData();
                kosong();
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "ID/Harga harus angka.");
        }// TODO add your handling code here:
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        if (txtIdJasa.getText().isEmpty()) return;
        int ok = JOptionPane.showConfirmDialog(this, "Hapus data ini?", "Konfirmasi",
                JOptionPane.YES_NO_OPTION);
        if (ok == JOptionPane.YES_OPTION) {
            try {
                int hasil = crud.hapusJasa(Integer.parseInt(txtIdJasa.getText()));
                if (hasil > 0) {
                    JOptionPane.showMessageDialog(this, "Data jasa dihapus");
                    tampilData();
                    kosong();
                }
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "ID harus angka.");
            }
        }// TODO add your handling code here:
    }//GEN-LAST:event_btnHapusActionPerformed

