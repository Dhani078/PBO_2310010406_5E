    private void tblKendaraanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKendaraanMouseClicked
        int baris = tblKendaraan.getSelectedRow();
        if (baris >= 0) {
            txtIdKendaraan.setText(tabmode.getValueAt(baris, 0).toString());
            txtIdPelanggan.setText(tabmode.getValueAt(baris, 1).toString());
            txtNopol.setText(tabmode.getValueAt(baris, 2).toString());
            cmbJenis.setSelectedItem(tabmode.getValueAt(baris, 3).toString());
            txtMerk.setText(tabmode.getValueAt(baris, 4).toString());
            txtWarna.setText(tabmode.getValueAt(baris, 5).toString());
        }// TODO add your handling code here:
    }//GEN-LAST:event_tblKendaraanMouseClicked

