    private void tampilData() {
        if (conn == null) return;

        String[] kolom = {"ID", "ID Pelanggan", "No Polisi", "Jenis", "Merk", "Warna"};
        tabmode = new DefaultTableModel(null, kolom);
        tblKendaraan.setModel(tabmode);

        try (Statement st = conn.createStatement();
             ResultSet rs = st.executeQuery("SELECT * FROM tb_kendaraan")) {
            while (rs.next()) {
                tabmode.addRow(new Object[]{
                    rs.getInt("id_kendaraan"),
                    rs.getString("id_pelanggan"),
                    rs.getString("no_polisi"),
                    rs.getString("jenis"),
                    rs.getString("merk"),
                    rs.getString("warna")
                });
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal tampil data: " + e.getMessage());
        }
    }

