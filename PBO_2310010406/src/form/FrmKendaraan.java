    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        int hasil = crud.simpanKendaraan(
            txtIdPelanggan.getText(),
            txtNopol.getText(),
            cmbJenis.getSelectedItem().toString(),
            txtMerk.getText(),
            txtWarna.getText()
        );
        if (hasil > 0) {
            JOptionPane.showMessageDialog(this, "Data kendaraan disimpan");
            tampilData(); kosong();
        }// TODO add your handling code here:
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        if (txtIdKendaraan.getText().isEmpty()) return;
        try {
            int hasil = crud.ubahKendaraan(
                Integer.parseInt(txtIdKendaraan.getText()),
                txtIdPelanggan.getText(),
                txtNopol.getText(),
                cmbJenis.getSelectedItem().toString(),
                txtMerk.getText(),
                txtWarna.getText()
            );
            if (hasil > 0) {
                JOptionPane.showMessageDialog(this, "Data kendaraan diubah");
                tampilData(); kosong();
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "ID Kendaraan harus angka.");
        }// TODO add your handling code here:
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        if (txtIdKendaraan.getText().isEmpty()) return;
        int ok = JOptionPane.showConfirmDialog(this, "Hapus data ini?", "Konfirmasi",
                JOptionPane.YES_NO_OPTION);
        if (ok == JOptionPane.YES_OPTION) {
            try {
                int hasil = crud.hapusKendaraan(Integer.parseInt(txtIdKendaraan.getText()));
                if (hasil > 0) {
                    JOptionPane.showMessageDialog(this, "Data kendaraan dihapus");
                    tampilData(); kosong();
                }
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "ID Kendaraan harus angka.");
            }
        }// TODO add your handling code here:
    }//GEN-LAST:event_btnHapusActionPerformed

