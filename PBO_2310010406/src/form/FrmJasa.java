    // ================== TAMPIL DATA KE TABEL ==================
    private void tampilData() {
        if (conn == null) return;

        String[] kolom = {"ID", "Nama Jasa", "Jenis Kendaraan", "Harga", "Keterangan"};
        tabmode = new DefaultTableModel(null, kolom);
        tblJasa.setModel(tabmode);

        try (Statement st = conn.createStatement();
             ResultSet rs = st.executeQuery("SELECT * FROM tb_jasa")) {

            while (rs.next()) {
                tabmode.addRow(new Object[]{
                    rs.getInt("id_jasa"),
                    rs.getString("nama_jasa"),
                    rs.getString("jenis_kendaraan"),
                    rs.getInt("harga"),
                    rs.getString("keterangan")
                });
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal tampil data: " + e.getMessage());
        }
    }

